# Basic Variables Demo
# Bu dosya Be My Code IDE ile ses komutları kullanılarak oluşturulmuştur

# String değişken
isim = ""

# Integer değişken
yas = 0

# Float değişken
boy = 0.0

# Boolean değişken
aktif = False

# Liste değişken
renkler = []

# Değerleri ata
isim = "Harun Efe Akkan"
yas = 22
boy = 1.75
aktif = True
renkler = ["kırmızı", "mavi", "yeşil"]

# Değerleri yazdır
print(f"İsim: {isim}")
print(f"Yaş: {yas}")
print(f"Boy: {boy}")
print(f"Aktif: {aktif}")
print(f"Renkler: {renkler}")
